package formes;

public class Point{
	protected double x;
	protected double y;
	protected double z;
	
	public Point (double x, double y, double z){
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public Point(){
		this(0,0,0); //initialise le point a 0
	}
	
	public double getX(){
		return this.x;
	}
	
	public double getY(){
		return this.y;
	}
	
	public double getZ(){
		return this.z;
	}
	
	public void setX(double x){
		this.x=x;
	}
	
	public void setY(double y){
		this.y=y;
	}
	
	public void setZ(double z){
		this.z=z;
	}
	
	@Override
	public String toString(){
		return ("point " + this.x + "," + this.y + "," + this.z);
	}
	
}
		
